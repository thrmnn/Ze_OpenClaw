#!/bin/bash
# Weekly review automation
# Generates summary note every Sunday evening

VAULT_PATH="/mnt/c/Users/theoh/OneDrive/Documents/Obsidian Vault"
WEEK_NUM=$(date +%U)
YEAR=$(date +%Y)
TODAY=$(date +%Y-%m-%d)
WEEK_START=$(date -d 'last monday' +%Y-%m-%d)
WEEK_END=$(date -d 'next sunday' +%Y-%m-%d)
REVIEW_NOTE="$VAULT_PATH/Daily/Weekly Review - Week $WEEK_NUM ($WEEK_START to $WEEK_END).md"

echo "📊 Generating weekly review for Week $WEEK_NUM..."

# Count Inbox items
