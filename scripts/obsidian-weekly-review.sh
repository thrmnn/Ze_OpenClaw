TRELLO_DONE=$(bash ~/clawd/scripts/trello-synthesis.sh 2>&1 | grep -i "done" || echo "No completed tasks found")

# QMD memory search for weekly highlights
export PATH="$HOME/.bun/bin:$PATH"
MEMORY_HIGHLIGHTS=$(qmd query "important decisions OR key insights OR lessons learned" -n 5 2>&1 | head -20 || echo "Memory search unavailable")

# Create weekly review note
cat > "$REVIEW_NOTE" << EOF
